<template>
  <section>
    <base-card>
      <header>
        <h2>Requests Received</h2>
      </header>
      <div v-if="isLoading">
        <base-spinner></base-spinner>
      </div>
      <ul v-else-if="hasRequests">
        <request-item
          v-for="message in messages"
          :key="message.id"
          :message="message.message"
          :email="message.email"
        ></request-item>
      </ul>
      <h3 v-else>You have no messages!</h3>
    </base-card>
  </section>
</template>

<script>
import RequestItem from '../../components/requests/RequestItem.vue';

export default {
  components: {
    RequestItem,
  },
  data() {
    return {
      isLoading: false
    };
  },
  computed: {
    messages() {
      return this.$store.getters['requests/requests'];
    },
    hasRequests() {
      return this.$store.getters['requests/hasRequests'];
    },
  },
  methods: {
    async loadRequests() {
      this.isLoading = true;
      await this.$store.dispatch('requests/fetchRequests');
      this.isLoading = false;
    },
  },
  created(){
    this.loadRequests();
  }
};
</script>

